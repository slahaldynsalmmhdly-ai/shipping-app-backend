// قائمة الدول فقط (للاستخدام في القوائم المنسدلة)
const arabCountries = Object.keys(arabCountriesAndCities);

// دالة للحصول على المدن حسب الدولة
const getCitiesByCountry = (country) => {
  return arabCountriesAndCities[country] || [];
};

// دالة للتحقق من صحة الدولة
const isValidCountry = (country) => {
  return arabCountries.includes(country);
};

// دالة للتحقق من صحة المدينة في دولة معينة
const isValidCity = (country, city) => {
  const cities = getCitiesByCountry(country);
  return cities.includes(city);
};

